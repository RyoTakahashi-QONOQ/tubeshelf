export function getCategoryBySlug(slug: string): Category | undefined {
  return categories.find((c) => c.slug === slug);
}

export function getVideoById(id: string): (Video & { category: Category }) | undefined {
  for (const cat of categories) {
    const video = cat.videos.find((v) => v.id === id);
    if (video) return { ...video, category: cat };
  }
  return undefined;
}

export function formatViews(views: number): string {
  if (views >= 10000) return `${Math.floor(views / 10000)}万回視聴`;
  if (views >= 1000) return `${(views / 1000).toFixed(1)}千回視聴`;
  return `${views}回視聴`;
}
